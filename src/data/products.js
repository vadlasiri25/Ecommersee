export const formatPrice = (price) => {
  return '₹' + price.toLocaleString('en-IN');
};

export const getProductById = (id) => {
  return products.find((p) => p.id === parseInt(id));
};

export const getProductsByCategory = (category) => {
  if (!category || category === 'all') return products;
  return products.filter((p) => p.category === category);
};

export const getRelatedProducts = (product, limit = 4) => {
  return products
    .filter((p) => p.id !== product.id && (p.category === product.category || p.gender === product.gender))
    .slice(0, limit);
};

export const searchProducts = (query) => {
  const q = query.toLowerCase();
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.brand.toLowerCase().includes(q) ||
      p.category.toLowerCase().includes(q)
  );
};

export const getBestSellers = () => {
  return products.filter(p => p.rating >= 4.4);
};

export const getTrending = () => {
  return products.filter(p => p.discount >= 50);
};

export const getNewArrivals = () => {
  return products.filter(p => p.id % 2 === 0);
};

export const getFlashSale = () => {
  return products.filter(p => p.discount >= 53);
};

export const getProductsByGender = (gender) => {
  if (!gender || gender === 'all') return products;
  return products.filter((p) => p.gender.toLowerCase() === gender.toLowerCase());
};

export const getProductsByBrand = (brand) => {
  if (!brand) return products;
  return products.filter((p) => p.brand.toLowerCase() === brand.toLowerCase());
};

export default products;
